package com.gsfellis;

public class Main {

    public static void main(String[] args) {
        int myVariable = 50; // Whole thing is a statement
// data type + expression = statement
        myVariable++; // statement
        myVariable--; // statement

        System.out.println("this is a test"); // statement

// all these lines are 1 statement
        System.out.printf("This is " +
                "another " +
                "statement");

        int x = 50;
        x++;  // 2 statements on the same line

    }
}
